package skia

import (
	"bytes"
	"testing"
)

func TestImage(t *testing.T) {
	t.Parallel()

	r := bytes.NewReader(pixelGif)
	data := NewData(r)
	image := NewEncodedImage(data)

	w, h := image.Width(), image.Height()
	if w != 1 || h != 1 {
		t.Fatalf("unexpected image size: %dx%d", w, h)
	}
}

var pixelGif = []byte{0x47, 0x49, 0x46, 0x38, 0x39, 0x61, 0x1, 0x0, 0x1, 0x0, 0x80, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x21, 0xf9, 0x4, 0x0, 0x0, 0x0, 0x0, 0x0, 0x2c, 0x0, 0x0, 0x0, 0x0, 0x1, 0x0, 0x1, 0x0, 0x0, 0x2, 0x2, 0x44, 0x1, 0x0, 0x3b}
